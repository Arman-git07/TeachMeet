
// This file must be in the public folder.
// It is a service worker that handles background push notifications.

// Scripts for firebase and messaging
importScripts('https://www.gstatic.com/firebasejs/9.0.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/9.0.0/firebase-messaging-compat.js');

// Your web app's Firebase configuration
const firebaseConfig = {
  apiKey: "YOUR_API_KEY", // Replace with your actual config
  authDomain: "YOUR_AUTH_DOMAIN",
  projectId: "YOUR_PROJECT_ID",
  storageBucket: "YOUR_STORAGE_BUCKET",
  messagingSenderId: "YOUR_MESSAGING_SENDER_ID",
  appId: "YOUR_APP_ID",
  measurementId: "YOUR_MEASUREMENT_ID"
};


// Note: This is a placeholder for the actual Firebase config.
// In a real application, you'd want a build step to inject the
// correct environment variables here, as service workers can't
// access `process.env`. For this prototype, this serves to
// illustrate the required structure. If you connect your own
// Firebase project, you must replace these placeholder values.
// The client-side `firebase.ts` will use the actual env vars.

try {
  firebase.initializeApp(firebaseConfig);
} catch (e) {
  console.error("Firebase service worker initialization error", e);
}


let messaging;
try {
  messaging = firebase.messaging();

  // Handle incoming messages. Called when:
  // - a message is received while the app has focus
  // - the user clicks on an notification generated by a service worker
  messaging.onBackgroundMessage((payload) => {
    console.log('[firebase-messaging-sw.js] Received background message ', payload);
    
    const notificationTitle = payload.notification.title;
    const notificationOptions = {
      body: payload.notification.body,
      icon: payload.notification.icon || '/logo-192.png' // Default icon
    };

    self.registration.showNotification(notificationTitle, notificationOptions);
  });
} catch (e) {
  console.error("Firebase messaging service worker error", e);
}
